    private void TableResultNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableResultNVMouseClicked
        selectedIndex = TableResultNV.getSelectedRow();
        NhanVien nv = listNhanVien.get(selectedIndex);
        txtIdStaff.setText(nv.getMaNhanVien());
        txtName.setText(nv.getTenNhanVien());
        String gen = nv.getGioiTinh();
        if (gen.equals("Nam")) {
            RdNam.setSelected(true);
        } else {
            RdNu.setSelected(true);
        }
        ChucVu cv = new ChucVuController().GetById(nv.getIDChucVu());
        int index = 0;
        int size = listChucVu.size();
        for (int i = 0; i < size; i++) {
            if (listChucVu.get(i).getID() == cv.getID()) {
                index = i;
            }
        }
        cmbChucVu.setSelectedIndex(index);
        Date date;
        if (!(new Check().isNullOrEmpty(nv.getNgaySinh()))) {
            try {
                date = new SimpleDateFormat("dd/MM/yyyy").parse(nv.getNgaySinh());
                txtDate.setDate(date);
            } catch (ParseException ex) {
                Logger.getLogger(QLKhachHang.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        txtAddress.setText(nv.getDiaChi());
        txtPhone.setText(nv.getSDT());
        txtEmail.setText(nv.getEmail());
        txtSalary.setText(nv.getLuong() + "");
    }//GEN-LAST:event_TableResultNVMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String name = txtSearch.getText();
        if (!(name.isBlank())) {
            listNhanVien = new NhanVienController().findByName(name);
            ModelNV.setRowCount(0);
            for (NhanVien nv : listNhanVien) {
                ChucVu cv = new ChucVuController().GetById(nv.getIDChucVu());
                cmbChucVu.setSelectedItem(cv);
                ModelNV.addRow(new Object[]{
                    TableResultNV.getRowCount() + 1,
                    nv.getMaNhanVien(),
                    nv.getTenNhanVien(),
                    nv.getGioiTinh(),
                    cv.getTenChucVu(),
                    nv.getNgaySinh(),
                    nv.getDiaChi(),
                    nv.getSDT(),
                    nv.getEmail(),
                    nv.getLuong(),});
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hãy nhập thông tin cần tìm kiếm!", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

