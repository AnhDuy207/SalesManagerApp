    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        var check = new Check();
        if (check.isNullOrEmpty(txtName.getText()) || check.isNullOrEmpty(txtIdStaff.getText()) || check.isNullOrEmpty(txtAddress.getText()) || check.isNullOrEmpty(txtPhone.getText())) {
            JOptionPane.showMessageDialog(this, "Cần nhập đầy đủ các thông tin cần thiết!", "Lỗi", JOptionPane.WARNING_MESSAGE);
        } else {
            NhanVien nv = new NhanVien();
            nv.setMaNhanVien(txtIdStaff.getText());
            nv.setTenNhanVien(txtName.getText());
            ChucVu cv = (ChucVu) cmbChucVu.getSelectedItem();
            nv.setIDChucVu(cv.getID());
            String date = null;
            if (txtDate.getDate() != null) {
                SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
                date = simpleDateFormat.format(txtDate.getDate());
            }
            nv.setNgaySinh(date);
            nv.setDiaChi(txtAddress.getText());
            nv.setEmail(txtEmail.getText());
            String gen = RdNam.isSelected() ? "Nam" : "Nữ";
            nv.setGioiTinh(gen);
            if (check.isDigit(txtPhone.getText())) {
                nv.setLuong(Integer.parseInt(txtPhone.getText()));
                if (check.isDigit(txtSalary.getText())) {
                    nv.setSDT(txtSalary.getText());
                    new NhanVienController().AddStaff(nv);
                    JOptionPane.showMessageDialog(this, "Thêm thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                    refresh();
                } else {
                    JOptionPane.showMessageDialog(this, "Lương phải là số!", "Lỗi", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "SĐT phải là số!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        selectedIndex = TableResultNV.getSelectedRow();
        if (listNhanVien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Danh sách nhân viên rỗng!", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn nhân viên mà bạn cần chỉnh sửa", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            var check = new Check();
            if (check.isNullOrEmpty(txtName.getText()) || check.isNullOrEmpty(txtIdStaff.getText()) || check.isNullOrEmpty(txtAddress.getText()) || check.isNullOrEmpty(txtPhone.getText())) {
                JOptionPane.showMessageDialog(this, "Cần nhập đầy đủ các thông tin cần thiết!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            } else {
                NhanVien nv = listNhanVien.get(selectedIndex);
                nv.setMaNhanVien(txtIdStaff.getText());
                nv.setTenNhanVien(txtName.getText());
                ChucVu cv = (ChucVu) cmbChucVu.getSelectedItem();
                nv.setIDChucVu(cv.getID());
                String date = null;
                if (txtDate.getDate() != null) {
                    SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
                    date = simpleDateFormat.format(txtDate.getDate());
                }
                nv.setNgaySinh(date);
                nv.setDiaChi(txtAddress.getText());
                nv.setEmail(txtEmail.getText());
                String gioitinh = RdNam.isSelected() ? "Nam" : "Nữ";
                nv.setGioiTinh(gioitinh);
                if (check.isDigit(txtPhone.getText())) {
                    nv.setLuong(Integer.parseInt(txtPhone.getText()));
                    if (check.isDigit(txtSalary.getText())) {
                        nv.setSDT(txtSalary.getText());
                        new NhanVienController().Update(nv);
                        JOptionPane.showMessageDialog(this, "Thêm thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                        refresh();
                    } else {
                        JOptionPane.showMessageDialog(this, "Lương phải là số!", "Lỗi", JOptionPane.WARNING_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "SĐT phải là số!", "Lỗi", JOptionPane.WARNING_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        selectedIndex = TableResultNV.getSelectedRow();
        if (listNhanVien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Danh sách nhân viên rỗng!", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn nhân viên mà bạn cần xóa!", "Lỗi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            NhanVien nv = listNhanVien.get(selectedIndex);
            new NhanVienController().DeleteStaff(nv.getID());
            JOptionPane.showMessageDialog(this, "Xóa thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            showTableNV();
            refresh();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

